package Array.Day02_Traversal;

public class ReplaceElement {
    public static void main(String[] args) {
        int[] sohvi = {10,20,30};

        for(int i = 0;i<sohvi.length;i++){
            sohvi[i] = 0;
        }

        for (int i = 0; i < sohvi.length; i++) {
           System.out.println(sohvi[i]);
        }
    }
}
